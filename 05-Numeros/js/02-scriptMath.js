/**
 * OBJETO MATH
 */
//Math es el objeto usado en JavaSript para funciones matemáticas.

let resultado;

//Método Minimo ==> min
resultado = Math.min(23,56, 78,89,23,56);
console.log("Min ", resultado);

//Método Maximo
resultado = Math.max(23,56, 78,89,23,56);
console.log("Max ", resultado);
 
//Método para redondear ==> round
resultado = Math.round(3.3);
console.log("round ", resultado);

//Método para obtener el valor absoluto ==> abs
resultado = Math.abs(-3.3);
console.log("abs ", resultado);

//Método para potencia de un número ==> pow
resultado = Math.pow(8, 3);
console.log("pow ", resultado);

//Método para obtener un valor randomico ==> rand
resultado = Math.random();
console.log("random ", resultado);

//Método para redondeo inferiro ==> floor
resultado = Math.floor(Math.random() * 10);
console.log("floor random ", resultado);






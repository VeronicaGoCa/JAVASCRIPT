/**
 * Estructura repetitiva
 * While
 * Se ejecuta mientras se cumple una condicion
 */
let k = 0;

while( k <= 5){
    console.log(` Numero : ${k}`);
    k ++;
}

/**
 * Do while
 * Nota.- do while se ejecuta al menos una vez
 */

let i = 0;
do{
    console.log(` Numero ${i}`)
    i++;

}while(i < 10 );
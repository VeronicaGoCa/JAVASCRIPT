/**
 * Operadores Aritméticos
 */
let resultado;
let valor1 = 20;
let valor2 = 6;

// Operador suma
resultado = valor1 + valor2;
console.log(resultado);
// Operador resta
resultado = valor1 - valor2;
console.log(resultado);
// Operador de multiplicación
resultado = valor1 * valor2;
console.log(resultado);
// Operador de división
resultado = valor1 / valor2;
console.log(resultado);
// Operador de Módulo
resultado = valor1 % valor2;
console.log(resultado);
